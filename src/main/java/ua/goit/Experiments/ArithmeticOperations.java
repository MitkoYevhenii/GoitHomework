package ua.goit.Experiments;

public class ArithmeticOperations {
    public static void main(String[] args) {
        int a = 5;
        double b = 0.4;

        System.out.println((a + b));
        System.out.println((a - b));
        System.out.println((a * b));
        System.out.println((a / b));
    }
}
